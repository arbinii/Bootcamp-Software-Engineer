void main(){
  var nominalPertama = 10;
  var nominalKedua = 4;
  //operator penjumlahan
  var hasilPenjumlahan = nominalPertama + nominalKedua;
  print('contoh operator penjumlahan $nominalPertama ditambah $nominalKedua samadengan $hasilPenjumlahan');
  //operator pengurangan
  var hasilPengurangan = nominalPertama - nominalKedua;
  print('contoh operator pengurangan $nominalPertama dikurangi $nominalKedua samadengan $hasilPengurangan');
  //operator negatif
  var hasilNegatif = -nominalPertama;
  print('contoh bilangan negatif $hasilNegatif');
  //operator perkalian
  var hasilKali = nominalPertama * nominalKedua;
  print('contoh operator perkalian $nominalPertama dikali $nominalKedua samadengan $hasilKali');
  //operator pembagian hasil double
  var hasilBagiDouble = nominalPertama / nominalKedua;
  print('contoh operator pembagian $nominalPertama dibagi $nominalKedua samadengan $hasilBagiDouble');
  //operator pembagian hasil int
  var hasilBagiInt = nominalPertama ~/ nominalKedua;
  print('contoh operator pembagian $nominalPertama dibagi $nominalKedua samadengan $hasilBagiInt');
  //operator habis bagi
  var hasilHabisBagi = nominalPertama % nominalKedua;
  print('contoh operator habis bagi $nominalPertama dibagi $nominalKedua samadengan $hasilHabisBagi');

}
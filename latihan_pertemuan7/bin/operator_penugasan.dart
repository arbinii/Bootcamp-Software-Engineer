void main (){
  var nominalTambah = 10;
  //tambah
  var cetakTambah = nominalTambah += 10;
  print(cetakTambah);
  //kurang
  var nominalKurang = 10;
  nominalKurang -= 10;
  print(nominalKurang);
  //kali
  var nominalKali = 10;
  nominalKali *= 10;
  print(nominalKali);
  //bagi double
  var nominalBagiDouble = 10.0;
  nominalBagiDouble /= 10.0;
  print(nominalBagiDouble);
  //bagi int
  var nominalBagiInt = 10;
  nominalBagiInt ~/= 10;
  print(nominalBagiInt);
  //hasil bagi
  var nominalHasilBagi = 10;
  nominalHasilBagi %= 10;
  print(nominalHasilBagi);

}